export type PosTranslationKey = keyof typeof posTranslations["es"];

export function usePosT() {
  const { language } = useLanguage();
  const t = useT("pos");

  return (key: PosTranslationKey): string => {
    if (process.env.NODE_ENV !== "production") {
      void language;
    }

    return t(String(key));
  };
}
